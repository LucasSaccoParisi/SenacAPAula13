// 3 - Crie uma matriz 20 x 20 de inteiros. Preencha com números aleatórios entre 10 e 99. Exiba então a soma dos valores de todas as linhas desta matriz, e informe qual linha possui o maior valor.

#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int numeroAleatorio(int comeco, int fim){

	int ale = rand() % (fim - comeco + 1) + comeco;

	return ale;
}

int main(){
    int linha = 20, coluna = 20;
    srand(time(NULL));
    int m[coluna][linha];
    int soma = 0;

    for(int i = 0; i < linha; i++){
        int numAlet = numeroAleatorio(10, 99);
        for(int j = 0; j < coluna; j++){
            m[i][j] = numAlet;
            printf("%d ", m[i][j]);
            soma += m[i][j];
        }
            printf("\n");
        }
        
        printf("\n%d \n", soma);
        
        return 0;
    }


